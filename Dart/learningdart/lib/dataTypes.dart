void dataType() {
  // String
  String name = "Ahmet";
  print(name);

  // int
  int oneNumber = 100;
  print(oneNumber);

  // double
  double oneDouble = 10.5;
  print(oneDouble);

  // bool
  bool IsTrue = true;

  // num
  num aNumber = 10;
  aNumber = 10.2;
  print(aNumber);

  // list
  List<String> names = ["Ahnmet", "Mehmet", "Sevda"];
  print(names);

  Set<String> days = {"Monday", "Tuesday"};
  print(days);

  // map
  Map<int, String> students = {1: "Ali", 2: "Mehmet", 3: "Sinem"};
  print(students[1]);
}
